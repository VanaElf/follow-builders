#!/usr/bin/env node

// ============================================================================
// Follow Builders — Format Digest (no AI needed)
// ============================================================================
// Reads prepare-digest.js output and formats it into a readable email.
// No LLM API required.

import { readFile } from 'fs/promises';
import { existsSync } from 'fs';

async function main() {
  const inputPath = process.argv[2];
  if (!inputPath || !existsSync(inputPath)) {
    console.error('Usage: node format-digest.js <input.json>');
    process.exit(1);
  }

  const data = JSON.parse(await readFile(inputPath, 'utf-8'));
  const lang = process.env.DIGEST_LANGUAGE || data.config?.language || 'zh';
  const now = new Date();
  const dateStr = lang === 'zh'
    ? `${now.getFullYear()}年${now.getMonth()+1}月${now.getDate()}日`
    : now.toLocaleDateString('en-US', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' });

  let output = '';

  if (lang === 'zh') {
    output += `AI Builders 动态 — ${dateStr}\n`;
    output += `================================\n\n`;

    // X/Twitter
    if (data.x && data.x.length > 0) {
      output += `🐦 推特动态\n`;
      output += `------------\n\n`;
      for (const builder of data.x) {
        if (!builder.tweets || builder.tweets.length === 0) continue;
        output += `【${builder.name}】@${builder.handle}\n`;
        if (builder.bio) output += `  ${builder.bio}\n`;
        for (const tweet of builder.tweets) {
          const text = tweet.text.replace(/\n/g, ' ').substring(0, 200);
          output += `  • ${text}${tweet.text.length > 200 ? '...' : ''}\n`;
          output += `    ${tweet.url}\n`;
        }
        output += `\n`;
      }
    }

    // Blogs
    if (data.blogs && data.blogs.length > 0) {
      output += `📝 官方博客\n`;
      output += `------------\n\n`;
      for (const blog of data.blogs) {
        output += `【${blog.title}】\n`;
        if (blog.content) {
          // Take first 300 chars as preview
          const preview = blog.content.replace(/\n+/g, ' ').substring(0, 300);
          output += `  ${preview}...\n`;
        }
        output += `  ${blog.url}\n\n`;
      }
    }

    // Podcasts
    if (data.podcasts && data.podcasts.length > 0) {
      output += `🎙️ 播客\n`;
      output += `------------\n\n`;
      for (const pod of data.podcasts) {
        output += `【${pod.name}】${pod.title}\n`;
        if (pod.transcript) {
          const preview = pod.transcript.replace(/\n+/g, ' ').substring(0, 300);
          output += `  ${preview}...\n`;
        }
        output += `  ${pod.url}\n\n`;
      }
    }

    output += `-- \n由 Follow Builders 生成: https://github.com/zarazhangrui/follow-builders\n`;

  } else {
    output += `AI Builders Digest — ${dateStr}\n`;
    output += `================================\n\n`;

    if (data.x && data.x.length > 0) {
      output += `🐦 X / Twitter\n`;
      output += `------------\n\n`;
      for (const builder of data.x) {
        if (!builder.tweets || builder.tweets.length === 0) continue;
        output += `[${builder.name}] @${builder.handle}\n`;
        if (builder.bio) output += `  ${builder.bio}\n`;
        for (const tweet of builder.tweets) {
          const text = tweet.text.replace(/\n/g, ' ').substring(0, 200);
          output += `  • ${text}${tweet.text.length > 200 ? '...' : ''}\n`;
          output += `    ${tweet.url}\n`;
        }
        output += `\n`;
      }
    }

    if (data.blogs && data.blogs.length > 0) {
      output += `📝 Blogs\n`;
      output += `------------\n\n`;
      for (const blog of data.blogs) {
        output += `[${blog.title}]\n`;
        if (blog.content) {
          const preview = blog.content.replace(/\n+/g, ' ').substring(0, 300);
          output += `  ${preview}...\n`;
        }
        output += `  ${blog.url}\n\n`;
      }
    }

    if (data.podcasts && data.podcasts.length > 0) {
      output += `🎙️ Podcasts\n`;
      output += `------------\n\n`;
      for (const pod of data.podcasts) {
        output += `[${pod.name}] ${pod.title}\n`;
        if (pod.transcript) {
          const preview = pod.transcript.replace(/\n+/g, ' ').substring(0, 300);
          output += `  ${preview}...\n`;
        }
        output += `  ${pod.url}\n\n`;
      }
    }

    output += `-- \nGenerated by Follow Builders: https://github.com/zarazhangrui/follow-builders\n`;
  }

  // Stats
  if (data.stats) {
    output += `\n📊 Stats: ${data.stats.xBuilders || 0} builders, ${data.stats.xTweets || 0} tweets, ${data.stats.podcasts || 0} podcasts, ${data.stats.blogs || 0} blogs\n`;
  }

  console.log(output);
}

main().catch(err => {
  console.error(`Fatal error: ${err.message}`);
  process.exit(1);
});
